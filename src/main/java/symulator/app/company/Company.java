package symulator.app.company;

public class Company {
    private double efficiency;
    private double profit;
    private double losses;
    private double employeeCounter;
    private double projectNumber;

    public double getEfficiency() {
        return efficiency;
    }

    public void setEfficiency(double efficiency) {
        this.efficiency = efficiency;
    }

    public double getProfit() {
        return profit;
    }

    public void setProfit(double profit) {
        this.profit = profit;
    }

    public double getLosses() {
        return losses;
    }

    public void setLosses(double losses) {
        this.losses = losses;
    }

    public double getEmployeeCounter() {
        return employeeCounter;
    }

    public void setEmployeeCounter(double employeeCounter) {
        this.employeeCounter = employeeCounter;
    }

    public double getProjectNumber() {
        return projectNumber;
    }

    public void setProjectNumber(double projectNumber) {
        this.projectNumber = projectNumber;
    }

}
